#printing pattern with integers
print("\n\tWELCOME TO THE PROGRAM")
print("we are going to make a parttern with integers with your entered number of rows.\n")
n=int(input("please enter the number of rows = "))
i=n
print("let's see the beautifull pattern...\n")
while i>=1:
	j=i
	while j>=1:
		print(j,end=" ")
		j -= 1
	print("\n")
	i -= 1









